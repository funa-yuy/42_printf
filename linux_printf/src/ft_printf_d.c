#include "../libft/libft.h"
#include "../ft_printf.h"

int	ft_printf_d(int d)
{
	size_t	i_len;
	char	*result;

	result = ft_itoa(d);
	i_len = ft_strlen(result);
	write(1, result, i_len);
	free(result);
	return (i_len);
}
